def sale_total(selling_price, quantity, discount=0):
    gross_total = float(selling_price) * float(quantity)
    return max(gross_total - float(discount or 0), 0)


def sale_profit(selling_price, buying_price, quantity, discount=0):
    gross_profit = (float(selling_price) - float(buying_price)) * float(quantity)
    return gross_profit - float(discount or 0)


def discount_is_valid(selling_price, quantity, discount):
    return 0 <= float(discount or 0) <= (float(selling_price) * float(quantity))



    goods_total_profit = sales["estimated_profit"].sum() if not sales.empty else 0
    total_profit = goods_total_profit + service_total_sales
    total_expenses = expenses["amount"].sum() if not expenses.empty else 0
    net_profit = total_profit - total_expenses
    total_purchases = purchases["total_cost"].sum() if not purchases.empty else 0


def create_staff_session(account, attendance_id):
    token = secrets.token_urlsafe(32)
    expires_at = (datetime.now() + timedelta(hours=18)).strftime("%Y-%m-%d %H:%M:%S")
    execute(
        """
        INSERT INTO app_sessions (
            token_hash, role, username, staff_account_id, attendance_id, expires_at
        )
        VALUES (%s, %s, %s, %s, %s, %s)
        """,
        (
            hash_session_token(token),
            account["role"],
            account["username"],
            int(account["staff_account_id"]),
            int(attendance_id),
            expires_at,
        ),
    )
    return token


def revoke_current_session():
    token = st.session_state.get("shift_token") or get_shift_token()
    if token:
        execute(
            """
            UPDATE app_sessions
            SET revoked_at = %s
            WHERE token_hash = %s AND revoked_at IS NULL
            """,
            (datetime.now().strftime("%Y-%m-%d %H:%M:%S"), hash_session_token(token)),
        )
    st.session_state.pop("shift_token", None)
    clear_shift_token()


def apply_staff_session(account, attendance_id, token=None):
    st.session_state.role = account["role"]
    st.session_state.username = account["username"]
    st.session_state.staff_account_id = int(account["staff_account_id"])
    st.session_state.attendance_id = int(attendance_id)
    st.session_state.employee_code = account["employee_code"]
    st.session_state.employee_name = account["employee_name"]
    st.session_state.employee_shop = account["shop"]
    if token:
        st.session_state.shift_token = token
        set_shift_token(token)


def restore_staff_session_from_token():
    if st.session_state.get("role"):
        return False
    token = get_shift_token()
    if not token:
        return False
    session = query_df(
        """
        SELECT s.session_id, s.role, s.username, s.staff_account_id, s.attendance_id,
               s.expires_at, a.employee_code, a.employee_name, a.shop,
               sa.is_active
        FROM app_sessions s
        JOIN staff_attendance a ON a.attendance_id = s.attendance_id
        JOIN staff_accounts sa ON sa.staff_account_id = s.staff_account_id
        WHERE s.token_hash = %s
          AND s.revoked_at IS NULL
          AND s.expires_at::timestamptz > now()
          AND a.clock_out_at IS NULL
        """,
        (hash_session_token(token),),
    )
    if session.empty:
        clear_shift_token()
        return False
    row = session.iloc[0].to_dict()
    if not row.get("is_active"):
        revoke_current_session()
        return False
    account = {
        "staff_account_id": row["staff_account_id"],
        "username": row["username"],
        "employee_code": row["employee_code"],
        "employee_name": row["employee_name"],
        "role": row["role"],
        "shop": row["shop"],
    }
    apply_staff_session(account, row["attendance_id"], token)
    return True



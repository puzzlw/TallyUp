def resolve_credit_customer(cur, payment_details):
    if payment_details["payment_method"] != "Credit":
        return None
    if payment_details["customer_id"]:
        return payment_details["customer_id"]
    customer_name = payment_details["new_customer_name"].strip()
    if not customer_name:
        raise ValueError("Customer name is required for credit sales.")
    return get_or_create_customer(cur, customer_name, payment_details["new_customer_phone"])



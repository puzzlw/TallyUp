def get_database_url():
    database_url = os.environ.get("DATABASE_URL", "")
    if not database_url:
        try:
            database_url = st.secrets.get("DATABASE_URL", "")
        except Exception:
            database_url = ""
    if not database_url:
        st.error("DATABASE_URL is missing from environment variables or .streamlit/secrets.toml.")
        st.stop()
    return database_url


@st.cache_resource(show_spinner=False)
def get_pool():
    return SimpleConnectionPool(1, 5, get_database_url(), sslmode="require")


def get_conn():
    return get_pool().getconn()


def release_conn(conn):
    get_pool().putconn(conn)


@st.cache_resource(show_spinner=False)
def init_db():
    conn = get_conn()
    cur = conn.cursor()

    cur.execute("""
    CREATE TABLE IF NOT EXISTS items (
        item_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
        item_name TEXT NOT NULL UNIQUE,
        buying_price REAL DEFAULT 0,
        selling_price REAL DEFAULT 0,
        current_stock REAL DEFAULT 0,
        created_at TEXT DEFAULT CURRENT_TIMESTAMP
    )
    """)

    cur.execute("""
    CREATE TABLE IF NOT EXISTS purchases (
        purchase_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
        purchase_date TEXT NOT NULL,
        item_id INTEGER NOT NULL,
        quantity REAL NOT NULL,
        buying_price REAL NOT NULL,
        total_cost REAL NOT NULL,
        created_at TEXT DEFAULT CURRENT_TIMESTAMP,
        FOREIGN KEY(item_id) REFERENCES items(item_id)
    )
    """)

    cur.execute("""
    CREATE TABLE IF NOT EXISTS sales (
        sale_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
        sale_date TEXT NOT NULL,
        item_id INTEGER NOT NULL,
        quantity REAL NOT NULL,
        selling_price REAL NOT NULL,
        total_amount REAL NOT NULL,
        estimated_profit REAL NOT NULL,
        created_at TEXT DEFAULT CURRENT_TIMESTAMP,
        FOREIGN KEY(item_id) REFERENCES items(item_id)
    )
    """)

    cur.execute("""
    CREATE TABLE IF NOT EXISTS expenses (
        expense_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
        expense_date TEXT NOT NULL,
        expense_name TEXT NOT NULL,
        amount REAL NOT NULL,
        remarks TEXT,
        created_at TEXT DEFAULT CURRENT_TIMESTAMP
    )
    """)

    cur.execute("""
    CREATE TABLE IF NOT EXISTS service_sales (
        service_sale_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
        sale_date TEXT NOT NULL,
        service_name TEXT NOT NULL,
        selling_price REAL NOT NULL,
        quantity REAL NOT NULL,
        total_amount REAL NOT NULL,
        created_at TEXT DEFAULT CURRENT_TIMESTAMP
    )
    """)

    cur.execute("ALTER TABLE items DROP COLUMN IF EXISTS category")
    cur.execute("ALTER TABLE items DROP COLUMN IF EXISTS unit")
    cur.execute("ALTER TABLE items DROP COLUMN IF EXISTS minimum_stock")
    cur.execute("ALTER TABLE purchases DROP COLUMN IF EXISTS supplier_name")
    cur.execute("ALTER TABLE purchases DROP COLUMN IF EXISTS payment_status")
    cur.execute("ALTER TABLE purchases DROP COLUMN IF EXISTS remarks")
    cur.execute("ALTER TABLE sales DROP COLUMN IF EXISTS buying_price_at_sale")
    cur.execute("ALTER TABLE sales DROP COLUMN IF EXISTS payment_method")
    cur.execute("ALTER TABLE sales DROP COLUMN IF EXISTS payment_status")
    cur.execute("ALTER TABLE sales DROP COLUMN IF EXISTS customer_name")
    cur.execute("ALTER TABLE sales DROP COLUMN IF EXISTS amount_paid")
    cur.execute("ALTER TABLE sales DROP COLUMN IF EXISTS balance")
    cur.execute("ALTER TABLE sales DROP COLUMN IF EXISTS remarks")
    cur.execute("DROP TABLE IF EXISTS stock_adjustments")

    conn.commit()
    cur.close()
    release_conn(conn)


@st.cache_data(ttl=30, show_spinner=False)
def query_df(sql, params=()):
    conn = get_conn()
    cur = conn.cursor(cursor_factory=RealDictCursor)
    try:
        cur.execute(sql, params)
        rows = cur.fetchall()
        columns = [desc.name for desc in cur.description] if cur.description else []
        return pd.DataFrame(rows, columns=columns)
    finally:
        cur.close()
        release_conn(conn)


def execute(sql, params=()):
    conn = get_conn()
    cur = conn.cursor()
    try:
        cur.execute(sql, params)
        conn.commit()
        last_id = cur.fetchone()[0] if cur.description else None
        st.cache_data.clear()
        return last_id
    except Exception:
        conn.rollback()
        raise
    finally:
        cur.close()
        release_conn(conn)


def get_items_df():
    return query_df("SELECT * FROM items ORDER BY item_name")


def get_item_options():
    df = get_items_df()
    if df.empty:
        return {}, []
    mapping = {row.item_name: int(row.item_id) for row in df.itertuples()}
    return mapping, list(mapping.keys())


def money(x):
    try:
        return f"{float(x):,.0f}"
    except Exception:
        return "0"


